    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Tombol Hapus
        int option = JOptionPane.showConfirmDialog(this,"Yakin ingin Hapus?","Hapus",JOptionPane.YES_NO_OPTION);
        if(option == 0){
            int id = Integer.parseInt(jComboBox1.getSelectedItem().toString());
            con.hapusRecord(id,"fasilitas_id","fasilitas");
            table();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Tombol edit
        int id = Integer.parseInt(jComboBox1.getSelectedItem().toString());
        ResultSet rs = con.ambilDataFasilitas(id);
        try{
            rs.next();
            String namafasilitas = rs.getString("fasilitas_nama");
            String fasilitasharga = rs.getString("fasilitas_harga");
            new EditFasilitas(state,id,namafasilitas,fasilitasharga).setVisible(true);
            this.dispose();
            
        }catch(SQLException e){
        
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

