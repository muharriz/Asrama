    private void table(){
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("ID Fasilitas");
        tb.addColumn("Nama Fasilitas");
        tb.addColumn("Harga per Satuan");
        tb.addColumn("Kuantitas");
        jTable1.setModel(tb);
        try{
             con = new Connectivity();
             rs = con.ExecQuery("select * from lihatsemuafasilitas");
             while(rs.next()){
                    tb.addRow(new Object[]{
                    rs.getString("fasilitas_id"),
                    rs.getString("fasilitas_nama"),
                    rs.getString("fasilitas_harga"),
                    rs.getString("kuantitas")
                    });
                }
        }catch(Exception ex){
             
                
        }
    }
    
